#!/bin/bash
set -e

# Bu script, Docker Compose için son .env dosyasını oluşturur.
# Kendi konumunu bularak, 'source' komutlarındaki göreceli yolları
# doğru bir şekilde çözer ve nihai KEY=VALUE çiftlerini yazar.

PROFILE=${1:-dev}

# --- DÜZELTME: Script'in kendi dizinini bularak yolları sağlamlaştırıyoruz ---
SCRIPT_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )
# SCRIPT_DIR -> /path/to/sentiric-infrastructure/scripts
INFRA_DIR=$(dirname "$SCRIPT_DIR")
# INFRA_DIR -> /path/to/sentiric-infrastructure
WORKSPACE_DIR=$(dirname "$INFRA_DIR")
# WORKSPACE_DIR -> /path/to/sentiric

CONFIG_DIR="${WORKSPACE_DIR}/sentiric-config/environments"
OUTPUT_FILE="${INFRA_DIR}/.env.generated"
SECRETS_FILE="${CONFIG_DIR}/common/secrets.env"
PROFILE_FILE="${CONFIG_DIR}/profiles/${PROFILE}.env"

if [ ! -f "$PROFILE_FILE" ]; then
    echo "❌ HATA: Profil dosyası bulunamadı: $PROFILE_FILE"
    exit 1
fi

echo "🔧 Yapılandırma dosyası '${OUTPUT_FILE}' oluşturuluyor (Profil: ${PROFILE})..."

# Çıktı dosyasını temizle ve başlık ekle
echo "# Auto-generated by Sentiric Orchestrator for profile: ${PROFILE}" > "$OUTPUT_FILE"
echo "# DO NOT EDIT THIS FILE MANUALLY" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

# Profil dosyasındaki tüm 'source' komutlarını oku ve işle
while IFS= read -r line || [[ -n "$line" ]]; do
    if [[ $line == source* ]]; then
        # `source ../common/platform.env` gibi bir satırı al
        # ve tam yolunu oluştur
        relative_path=$(echo "$line" | cut -d' ' -f2)
        # Profil dosyasının bulunduğu dizine göre yolu çöz
        source_file_path="${CONFIG_DIR}/profiles/${relative_path}"
        
        if [ -f "$source_file_path" ]; then
            # Dosyadaki yorumları ve boş satırları atla, geri kalanını ekle
            grep -vE '^\s*#|^\s*$' "$source_file_path" >> "$OUTPUT_FILE"
            echo "" >> "$OUTPUT_FILE" # Okunabilirlik için boşluk ekle
        else
            echo "⚠️ UYARI: Kaynak dosyası bulunamadı, atlanıyor: $source_file_path"
        fi
    fi
done < "$PROFILE_FILE"

# Sırları da ekle, eğer varsa
if [ -f "$SECRETS_FILE" ]; then
    echo "# Included from: secrets.env" >> "$OUTPUT_FILE"
    grep -vE '^\s*#|^\s*$' "$SECRETS_FILE" >> "$OUTPUT_FILE"
    echo "" >> "$OUTPUT_FILE"
fi

# Dinamik değişkenleri ekle
echo "# Dynamically added by Orchestrator" >> "$OUTPUT_FILE"
# DÜZELTME: WSL için daha güvenilir IP bulma yöntemi
DETECTED_IP=$(hostname -I | awk '{print $1}')
echo "PUBLIC_IP=${DETECTED_IP}" >> "$OUTPUT_FILE"
TAG=${TAG:-latest}
echo "TAG=${TAG}" >> "$OUTPUT_FILE"
echo "CONFIG_REPO_PATH=../sentiric-config" >> "$OUTPUT_FILE"

# Sonuç dosyası içindeki potansiyel Windows satır sonlarını (CR) temizle
if command -v dos2unix &> /dev/null; then
    dos2unix "$OUTPUT_FILE"
elif command -v sed &> /dev/null; then
    sed -i 's/\r$//' "$OUTPUT_FILE"
fi

echo "✅ Yapılandırma başarıyla oluşturuldu."